package com.example.dto;

public class Product {

	private String PrdCode;
	private String PrdName;
	private int PrdPrice;
	private int PrdQuantity;
	private int PrdCategory;
	private String PrdColor;
	private String PrdSize;
	private int PrdSafeStock;
	private int PrdInstallTime;


	
	public String getPrdCode() {
		return PrdCode;
	}
	public void setPrdCode(String prdCode) {
		PrdCode = prdCode;
	}
	public String getPrdName() {
		return PrdName;
	}
	public void setPrdName(String prdName) {
		PrdName = prdName;
	}
	public int getPrdPrice() {
		return PrdPrice;
	}
	public void setPrdPrice(int prdPrice) {
		PrdPrice = prdPrice;
	}
	public int getPrdQuantity() {
		return PrdQuantity;
	}
	public void setPrdQuantity(int prdQuantity) {
		PrdQuantity = prdQuantity;
	}
	public int getPrdCategory() {
		return PrdCategory;
	}
	public void setPrdCategory(int prdCategory) {
		PrdCategory = prdCategory;
	}
	public String getPrdColor() {
		return PrdColor;
	}
	public void setPrdColor(String prdColor) {
		PrdColor = prdColor;
	}
	public String getPrdSize() {
		return PrdSize;
	}
	public void setPrdSize(String prdSize) {
		PrdSize = prdSize;
	}
	public int getPrdSafeStock() {
		return PrdSafeStock;
	}
	public void setPrdSafeStock(int prdSafeStock) {
		PrdSafeStock = prdSafeStock;
	}
	public int getPrdInstallTime() {
		return PrdInstallTime;
	}
	public void setPrdInstallTime(int prdInstallTime) {
		PrdInstallTime = prdInstallTime;
	}

}
