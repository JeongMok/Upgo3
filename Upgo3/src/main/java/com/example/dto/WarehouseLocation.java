package com.example.dto;

public class WarehouseLocation {

	private int wlNo;
	private int warehouseNo;
	private String prdCode;
	private int wlQuantity;
	
	public int getWlNo() {
		return wlNo;
	}
	public void setWlNo(int wlNo) {
		this.wlNo = wlNo;
	}
	public int getWarehouseNo() {
		return warehouseNo;
	}
	public void setWarehouseNo(int warehouseNo) {
		this.warehouseNo = warehouseNo;
	}
	public String getPrdCode() {
		return prdCode;
	}
	public void setPrdCode(String prdCode) {
		this.prdCode = prdCode;
	}
	public int getWlQuantity() {
		return wlQuantity;
	}
	public void setWlQuantity(int wlQuantity) {
		this.wlQuantity = wlQuantity;
	}
	

}
